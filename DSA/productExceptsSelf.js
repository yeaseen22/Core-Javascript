function productExceptSelf(nums) {
    const n = nums.length;
    const result = new Array(n).fill(1);
    
    // প্রিফিক্স প্রোডাক্ট গণনা
    let prefix = 1;
    for (let i = 0; i < n; i++) {
        result[i] = prefix; // বর্তমান ইনডেক্সের জন্য প্রিফিক্স প্রোডাক্ট
        prefix *= nums[i]; // পরবর্তী ইনডেক্সের জন্য প্রিফিক্স আপডেট
    }
    
    // সাফিক্স প্রোডাক্ট গণনা
    let suffix = 1;
    for (let i = n - 1; i >= 0; i--) {
        result[i] *= suffix; // বর্তমান ইনডেক্সের জন্য সাফিক্স প্রোডাক্ট গুণ
        suffix *= nums[i]; // পরবর্তী ইনডেক্সের জন্য সাফিক্স আপডেট
    }
    
    return result;
}

